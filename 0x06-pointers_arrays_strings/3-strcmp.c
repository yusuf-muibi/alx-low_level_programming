#include "main.h"
/**
* _strcmp - this is the main function that compares the strings
* @s1: this is the first string to be compared
* @s2: this is the second string ot be compared
* Return: this returns the result of the comparison
*/
int _strcmp(char *s1, char *s2)
{
int t;
for (t = 0; s1[t] != '\0' || s2[t] != '\0'; t++)
{
if (s1[t] != s2[t])
{
if (s1[t] < s2[t])
return (s1[t] - s2[t]);
else if (s1[t] > s2[t])
return (s1[t] - s2[t]);
}
}
return (0);
}
